#include <iostream>
using namespace std;
// Operators are symbols that perform operations on variables and values


// Arithmetic Operator

int main(){
    int a, b;
    a = 7;
    b = 2;


    // printing the sum of a by b
    cout << "a + b = " << (a + b) << endl;
    
    // printing the difference of a by b
    cout << "a - b = " << (a - b) << endl;

    // printing the product of a by b
    cout << "a * b = " << (a * b) << endl;

    // printing the division of a by b
    cout << "a / b = " << (a / b) << endl;

    // printing the modulo of a by b
    cout << "a % b = " << (a % b) << endl;


}
